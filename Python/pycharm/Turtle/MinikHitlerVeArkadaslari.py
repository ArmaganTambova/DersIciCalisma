import turtle
# Nazi Almanyasını ve Hitleri Savunmuyorum!!! Bu Nokta Çok Önemli Lütfen Saçma Salak Yorumlar Yapmayın!
ok = turtle.Turtle()
ok.pensize(3)
ok.color("red", "red")
ok.pensize(20)
i = 1
while i <= 4:
    ok.right(90)
    ok.forward(200)
    ok.right(90)
    ok.forward(200)
    ok.backward(200)
    ok.right(90)
    ok.forward(200)
    i += 1
turtle.mainloop()